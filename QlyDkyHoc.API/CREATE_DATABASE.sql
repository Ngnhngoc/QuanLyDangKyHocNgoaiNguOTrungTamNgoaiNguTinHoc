BEGIN
    PRINT 'Legacy incompatible schema detected. Recreating managed QlyDkyHoc tables.';

    DECLARE @dropFkSql NVARCHAR(MAX) = N'';
    SELECT @dropFkSql = @dropFkSql
        + N'ALTER TABLE ' + QUOTENAME(OBJECT_SCHEMA_NAME(parent_object_id))
        + N'.' + QUOTENAME(OBJECT_NAME(parent_object_id))
        + N' DROP CONSTRAINT ' + QUOTENAME(name) + N';'
    FROM sys.foreign_keys
    WHERE parent_object_id IN (
        OBJECT_ID(N'dbo.PHIEUTHANHTOAN'), OBJECT_ID(N'dbo.DANGKY'), OBJECT_ID(N'dbo.GIANGDAY'),
        OBJECT_ID(N'dbo.QUANLY'), OBJECT_ID(N'dbo.HOCVIEN'), OBJECT_ID(N'dbo.LOPHOC'),
        OBJECT_ID(N'dbo.KHOAHOC'), OBJECT_ID(N'dbo.GIANGVIEN'), OBJECT_ID(N'dbo.NHANVIEN'),
        OBJECT_ID(N'dbo.DANHMUCNN')
    )
    OR referenced_object_id IN (
        OBJECT_ID(N'dbo.PHIEUTHANHTOAN'), OBJECT_ID(N'dbo.DANGKY'), OBJECT_ID(N'dbo.GIANGDAY'),
        OBJECT_ID(N'dbo.QUANLY'), OBJECT_ID(N'dbo.HOCVIEN'), OBJECT_ID(N'dbo.LOPHOC'),
        OBJECT_ID(N'dbo.KHOAHOC'), OBJECT_ID(N'dbo.GIANGVIEN'), OBJECT_ID(N'dbo.NHANVIEN'),
        OBJECT_ID(N'dbo.DANHMUCNN')
    );

    IF LEN(@dropFkSql) > 0 EXEC sp_executesql @dropFkSql;

    DROP TABLE IF EXISTS dbo.PHIEUTHANHTOAN;
    DROP TABLE IF EXISTS dbo.DANGKY;
    DROP TABLE IF EXISTS dbo.GIANGDAY;
    DROP TABLE IF EXISTS dbo.QUANLY;
    DROP TABLE IF EXISTS dbo.HOCVIEN;
    DROP TABLE IF EXISTS dbo.LOPHOC;
    DROP TABLE IF EXISTS dbo.KHOAHOC;
    DROP TABLE IF EXISTS dbo.GIANGVIEN;
    DROP TABLE IF EXISTS dbo.NHANVIEN;
    DROP TABLE IF EXISTS dbo.DANHMUCNN;
END
